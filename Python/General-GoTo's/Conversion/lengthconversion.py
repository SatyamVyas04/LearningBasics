def miletokm(a):
    """Converts input from miles to km"""
    km = a * 1.61
    return km


def kmtomile(a):
    """Converts input from km to miles"""
    miles = a * 0.62
    return miles


def inchtofeet(a):
    """Converts input from inches to feet"""
    feet = a / 12
    return feet


def feettoinch(a):
    """Converts input from feet to inches"""
    inches = a * 12
    return inches

